#include "minishell.h"

void	ft_freestrs(char **s)
{
	int	i;

	i = 0;
	while (s && s[i])
	{
		free(s[i]);
		i++;
	}
	free(s);
}

int	ft_is_path(char *cmdname)
{
	while (*cmdname)
	{
		if (*cmdname == '/')
			return (1);
		cmdname++;
	}
	return (0);
}
